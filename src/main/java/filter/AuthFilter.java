    private String getCookieValue(HttpServletRequest req, String name) {
        Cookie[] cookies = req.getCookies();
        if (cookies != null) {
            for (Cookie c : cookies) {
                if (c.getName().equals(name)) {
                    return c.getValue();
                }
            }
        }
        return null;
    }

    /**
     *
     * @param request The servlet request we are processing
     * @param response The servlet response we are creating
     * @param chain The filter chain we are processing
     *
     * @exception IOException if an input/output error occurs
     * @exception ServletException if a servlet error occurs
     */
    public void doFilter(ServletRequest request, ServletResponse response,
            FilterChain chain)
            throws IOException, ServletException {
     HttpServletRequest req = (HttpServletRequest) request;
    HttpServletResponse res = (HttpServletResponse) response;

    String role = getCookieValue(req, "cookieRole");
    String uri = req.getServletPath();

    // 0. Ngoại lệ: Cho phép tất cả mọi người vào trang Login/Register/Trang chủ
    // Nếu không có dòng này, người chưa đăng nhập sẽ bị loop redirect mãi mãi
//    if (uri.equals("/userservlet") || uri.endsWith("login.jsp")) {
//        chain.doFilter(request, response);
//        return;
//    }

    // 1. Kiểm tra đăng nhập (nếu vào các trang cần quyền mà chưa có cookie)
    if (role == null) {
        role = "";
    }

    // 2. Định nghĩa logic chặn
    boolean isAccessDenied = false;

    if (role.equals("customer")) {
        // Customer: Chặn staff và admin
        if (uri.equals("/staffservlet") || uri.equals("/staffTemplate.jsp") ||
            uri.equals("/adminservlet") || uri.equals("/adminTemplate.jsp")) {
            isAccessDenied = true;
        }
    } else if (role.equals("Staff")) {
        // Staff: Chặn admin
        if (uri.equals("/adminservlet") || uri.equals("/adminTemplate.jsp")) {
            isAccessDenied = true;
        }
    } else if (role.equals("Admin")) {
        // Admin: Chặn staff (theo ý bạn)
        if (uri.equals("/staffservlet") || uri.equals("/staffTemplate.jsp")) {
            isAccessDenied = true;
        }
    } else {
        if (uri.equals("/staffservlet") || uri.equals("/staffTemplate.jsp") ||
            uri.equals("/adminservlet") || uri.equals("/adminTemplate.jsp")) {
            isAccessDenied = true;
        }
    } 

    // 3. Xử lý kết quả
    if (isAccessDenied) {
        // Thay vì redirect về home, bạn có thể tạo trang 403.jsp để thông báo "Không có quyền"
        res.sendRedirect("userservlet?action=homePage");
    } else {
        chain.doFilter(request, response);
    }
    }

